public class Timetable
{
        public static void main(String[] args)
        {
            int i=1;
            int j=2;
            int k=10;

            //do while loop for calculation
            do
            {
                // i value keep incremented by 1 upto it reach at 10 as i mention i<=k
                System.out.println(i +" * "+ j +" = "+(i*j));
                i++;
            }
            while(i<=k); //so it will keep executing till i value becomes 10.
        }

    }
